package basicvprogram2;

public class Math_Class 
{
	public static void main(String[] args) 
	{
		System.out.println(Math.PI);
		System.out.println(Math.addExact(5, 10));
		System.out.println(Math.subtractExact(10, 5));
		System.out.println(Math.multiplyExact(5, 5));
		System.out.println(Math.sqrt(9));
		System.out.println(Math.max(555.34, 600.34));
		System.out.println(Math.min(10, 5));
		System.out.println(Math.random());
	}

}

package CollectionandOther;
public class Switch_case 
{
	public static void main(String[] args)
	{
		switch(6)
		{
			case 1:
				System.out.println("launch google chrome");
				break;
			case 2:
				System.out.println("launch microsoft");
				break;
			case 3:
				System.out.println("launch edge browser");
				break;
			case 4:
				System.out.println("launch internet");
				break;
			case 5:
				System.out.println("launch google");
				break;
				default:
				System.out.println("sorry wrong option");
		}
	}
}

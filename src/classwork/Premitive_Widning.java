package classwork;
public class Premitive_Widning
{
	public static void main(String[] args) 
	{
		double age = 10;
		System.out.println(age);
		double ageofperson = age;   // implicit way  converting int into double
		System.out.println(ageofperson);
		
		double ageofperson1 = (double) age;   // explicit way
		System.out.println(ageofperson1);
		
		
		

	}

}
